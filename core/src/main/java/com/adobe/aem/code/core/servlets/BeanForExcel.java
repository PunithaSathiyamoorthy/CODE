package com.adobe.aem.code.core.servlets;


public class BeanForExcel {
    private String damAttribute;
    private String dam;
    private String region;

    public String getDamAttribute() {
        return damAttribute;
    }

    public String getDam() {
        return dam;
    }

    public String getRegion() {
        return region;
    }

    public void setDamAttribute(String damAttribute) {
        this.damAttribute = damAttribute;
    }

    public void setDam(String dam) {
        this.dam = dam;
    }

    public void setRegion(String region) {
        this.region = region;
    }

}

